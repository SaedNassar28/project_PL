   protected void setPanelData(TeachingAssistant x) {
        jTextFieldID.setText("" + x.getID());
        jTextFieldFname.setText("" + x.getFName());
        jTextFieldLname.setText("" + x.getLName());
        jTextFieldUserName.setText("" + x.getUserName());
        jPasswordField1.setText("" + x.getPass());
        jTextField1.setText(""+ x.getAcademicHours());
        jTextFieldSalary.setText(""+ x.getSalary());
        jTextField2.setText(""+ x.getAge());
        


         if (x.myDepart.getName().equals("CS")) {
            jComboBoxDept1.setSelectedIndex(0);
        } else if (x.myDepart.getName().equals("IS")) {
            jComboBoxDept1.setSelectedIndex(1);
        } else if (x.myDepart.getName().equals("IT")) {
            jComboBoxDept1.setSelectedIndex(2);
        } else if (x.myDepart.getName().equals("SW")) {
            jComboBoxDept1.setSelectedIndex(3);
        }

    }
   protected void resetPanelData() {
        jTextFieldID.setText("");
        jTextFieldFname.setText("");
        jTextFieldLname.setText("");
        jTextFieldUserName.setText("");
        jTextField1.setText("");
        jPasswordField1.setText("");        
        jTextFieldSalary.setText("");
        jTextField2.setText("");
        jComboBoxDept1.setSelectedIndex(0);
    }


